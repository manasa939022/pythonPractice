# empty list
print(list())

# vowel string
vowel_string = 'aeiou'
print(list(vowel_string))

# vowel tuple
vowel_tuple = ('a', 'e', 'i', 'o', 'u')
print(list(vowel_tuple))

#vowel list
vowel_list = ['1', '3', '5', '7', '9']
print(list(vowel_list))

#vowel dictionary
vowel_dictionary = {'a': 1, 'e': 2, 'i': 3, 'o': 4, 'u': 5 }
print(list(vowel_dictionary))